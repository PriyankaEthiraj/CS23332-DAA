#include<stdio.h>
 void factor(int num)
{
    int count=0;
    for(int i=1;i<=num;i++)
    {
        count++;
        if(num%i==0)
        {
            count++;
            //printf("%d",i);
        }
        count++;
    }
    count++;
    printf("%d",count);
}
int main()
{
    int a;
    scanf("%d",&a);
    factor(a);
}
